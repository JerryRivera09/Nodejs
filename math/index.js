export function sumar(x, y) {
  return x + y;
}
export function resta(x, y) {
  return x - y;
}
export function multiplicacion(x, y) {
  return x * y;
}
export function divide(x, y) {
  return x / y;
}

export default {
  sumar,
  resta,
  multiplicacion,
  divide
};

